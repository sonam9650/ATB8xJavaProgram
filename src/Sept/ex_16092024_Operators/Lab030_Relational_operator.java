package Sept.ex_16092024_Operators;

public class Lab030_Relational_operator {
    public static void main(String[] args) {
        //Relational operator = boolean value result
        // <,>,>=,<=,!=,==
        int a=10;
        int b=20;
        boolean c=a>b;
        System.out.println(c);

        int age=34;
        int age1=34;
        boolean result=age>age1;
        boolean result1=age>=age1;
        System.out.println(result);
        System.out.println(result1);
    }
}
