package Sept.ex_16092024_Operators;

public class Lab031_Logical_operators {
    public static void main(String[] args) {
        //Logical operators=!-NOT,&&-AND,||-OR
        boolean a=true;
        boolean b=false;
        boolean c=a && b;
        boolean c1=a||b;
        System.out.println(c);
        System.out.println(c1);
        System.out.println(!c);
    }
}
