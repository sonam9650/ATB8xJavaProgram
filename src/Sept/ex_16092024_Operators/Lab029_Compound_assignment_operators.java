package Sept.ex_16092024_Operators;

public class Lab029_Compound_assignment_operators {
    public static void main(String[] args) {
        //compound Assignment operators (+=,-=,*=,/=,%=)
        int a =10;
         a+=20;  //a=a+20
        a-=20; //a=a-20
        a/=20; //a=a/20
        a*=20; //a=a*20
        System.out.println(a);

    }
}
